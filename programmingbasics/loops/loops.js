// Opdracht 1: Maak een while loop die kijkt of een in een reeks getallen deelbaar zijn door 4.

var x = 1;

while (x <= 30) {
    if (x % 4 == 0){
	console.log(x + " dit nummer is deelbaar door 4");
    }else {
	console.log(x + " dit nummer is niet deelbaar door 4");
    }
    x ++
}



